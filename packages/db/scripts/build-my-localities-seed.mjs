// Regenerates packages/db/seeds/my-localities.sql from the upstream
// Malaysia postcodes dataset. Re-run if the source updates.
//
//   node packages/db/scripts/build-my-localities-seed.mjs
//
// Source: https://github.com/AsyrafHussin/malaysia-postcodes (MIT)
// Snapshot kept at packages/db/seeds/_source/malaysia-postcodes.json so the
// build is offline and deterministic.

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const SRC  = resolve(HERE, '../seeds/_source/malaysia-postcodes.json');
const OUT  = resolve(HERE, '../seeds/my-localities.sql');

// Normalized state name → 3-letter state_code. The dataset prefixes federal
// territories with "Wp " (Wilayah Persekutuan); we strip it for display so
// staff see "Kuala Lumpur", not "Wp Kuala Lumpur".
const STATE_CODE = {
  'Johor':           'JHR',
  'Kedah':           'KDH',
  'Kelantan':        'KTN',
  'Kuala Lumpur':    'KUL',
  'Labuan':          'LBN',
  'Melaka':          'MLK',
  'Negeri Sembilan': 'NSN',
  'Pahang':          'PHG',
  'Perak':           'PRK',
  'Perlis':          'PLS',
  'Pulau Pinang':    'PNG',
  'Putrajaya':       'PJY',
  'Sabah':           'SBH',
  'Sarawak':         'SWK',
  'Selangor':        'SGR',
  'Terengganu':      'TRG',
};

const normalizeState = (raw) => raw.replace(/^Wp\s+/i, '').trim();
const sqlStr = (s) => "'" + String(s).replace(/'/g, "''") + "'";

const data = JSON.parse(readFileSync(SRC, 'utf8'));

const rows = [];
for (const state of data.state) {
  const name = normalizeState(state.name);
  const code = STATE_CODE[name];
  if (!code) throw new Error(`Unknown state: ${state.name} → ${name}`);
  for (const city of state.city) {
    for (const postcode of city.postcode) {
      rows.push({ postcode, city: city.name, state: name, stateCode: code });
    }
  }
}
rows.sort((a, b) =>
  a.state.localeCompare(b.state) ||
  a.city.localeCompare(b.city) ||
  a.postcode.localeCompare(b.postcode),
);

const CHUNK = 500;
const lines = [];
lines.push(`-- packages/db/seeds/my-localities.sql`);
lines.push(`-- ----------------------------------------------------------------------------`);
lines.push(`-- Malaysia postcode dataset for my_localities (state → city → postcode cascade).`);
lines.push(`-- Source: https://github.com/AsyrafHussin/malaysia-postcodes (MIT)`);
lines.push(`-- Generated by: packages/db/scripts/build-my-localities-seed.mjs`);
lines.push(`-- Regenerate by: node packages/db/scripts/build-my-localities-seed.mjs`);
lines.push(`-- Rows: ${rows.length} postcode rows across 16 states/territories`);
lines.push(`-- ----------------------------------------------------------------------------`);
lines.push(``);
lines.push(`BEGIN;`);
lines.push(`TRUNCATE my_localities;`);
lines.push(``);

for (let i = 0; i < rows.length; i += CHUNK) {
  const slice = rows.slice(i, i + CHUNK);
  lines.push(`INSERT INTO my_localities (postcode, city, state, state_code) VALUES`);
  slice.forEach((r, idx) => {
    const suffix = idx === slice.length - 1 ? ';' : ',';
    lines.push(`  (${sqlStr(r.postcode)}, ${sqlStr(r.city)}, ${sqlStr(r.state)}, ${sqlStr(r.stateCode)})${suffix}`);
  });
  lines.push(``);
}

lines.push(`COMMIT;`);
lines.push(``);

writeFileSync(OUT, lines.join('\n'), 'utf8');
console.log(`Wrote ${rows.length} rows → ${OUT}`);
